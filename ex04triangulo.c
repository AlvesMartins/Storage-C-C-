/*
Criar um programa para calcular a área de um triângulo retângulo.
*/

#include<stdio.h>

int main(){
    float base, altura, area;

    base = 7.5;
    altura = 3.0;

    area = base * altura / 2.0;

    printf("A area do triangulo retangulo de base %.2f e altura %.2f é igual a %.3f.\n", base, altura, area);

    return 0;
}











